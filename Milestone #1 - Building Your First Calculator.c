#include <stdio.h>
int main()
{
    // Addition
    printf("%d + %d = %d\n", 5, 3, 5 + 3);
    // Subtraction
    printf("%d - %d = %d\n", 5, 3, 5 - 3);
    // Multiplication
    printf("%d * %d = %d\n", 5, 3, 5 * 3);
    // Division
    printf("%d / %d = %d\n", 5, 3, 5 / 3);
    // Remainder
    printf("%d %% %d = %d\n", 5, 3, 5 % 3);
    return 0;
}